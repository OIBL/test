function generate(id, map) {
  const absolute_card = document.getElementById(id);

  if (!absolute_card) { return };

  map.forEach((excuse) => {
    const new_card = document.createElement('div')

    new_card.className = 'card'

    new_card.innerHTML = `
        <h3>${excuse.title}</h3>
        <p class="opisanie">${excuse.description}</p>
  `
    absolute_card.appendChild(new_card);
  });
};

