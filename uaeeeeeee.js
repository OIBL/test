document.querySelectorAll('.card').forEach((card) => {
  card.addEventListener('click', (e) => {
    // если кликнули НЕ по внутренней ссылке
    if (!e.target.closest('.inner-link')) {
      window.location.href = card.dataset.href;
    }
  });
});








